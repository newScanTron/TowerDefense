import Foundation
import SpriteKit

class EnemyMoveStrat
{

    func Move(sprite: SKSpriteNode){
        
    }
    
    func random() -> CGFloat{
        return CGFloat(Float(arc4random()) / 0xFFFFFFFF)
    }
    
    func random(min min: CGFloat, max: CGFloat) -> CGFloat{
        return random() * (max - min) + min
    }
}